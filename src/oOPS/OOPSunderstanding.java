package oOPS;

public class OOPSunderstanding {
	// OOPS -> Object Oriented Programming concept
		// 1. Inheritance -> ability of child accessing parent property with no restriction
		// 2. Method Overriding -> same method present in child and parent, child method overrides parent method
		// 3. Polymorphism -> object having different form
		// 4. Encapsulation -> data hiding
		// 5. Abstraction -> different class/method than regular
		// 6. Interface -> for high level rules
}
